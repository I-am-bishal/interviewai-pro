/* ─── FAQ Item Accordion ─────────────────────────────────────────────────── */
const FAQItem = ({ q, a, i }) => {
  const [open, setOpen] = useState(false);
  const [mousePos, setMousePos] = useState({ x: 0, y: 0 });
  const [isHovered, setIsHovered] = useState(false);
  const [particles, setParticles] = useState([]);

  const handleMouseMove = (e) => {
    const rect = e.currentTarget.getBoundingClientRect();
    setMousePos({ x: e.clientX - rect.left, y: e.clientY - rect.top });
  };

  const handleToggle = (e) => {
    setOpen(!open);
    
    // Spawn particle burst at click position
    const rect = e.currentTarget.getBoundingClientRect();
    const x = e.clientX - rect.left;
    const y = e.clientY - rect.top;
    
    const colors = ['#6366f1', '#06b6d4', '#a855f7', '#ec4899', '#3b82f6'];
    const newParticles = Array.from({ length: 8 }).map((_, idx) => {
      const angle = (idx / 8) * Math.PI * 2 + (Math.random() - 0.5) * 0.4;
      const distance = 40 + Math.random() * 50;
      const dx = Math.cos(angle) * distance;
      const dy = Math.sin(angle) * distance;
      return {
        id: Math.random(),
        x,
        y,
        dx,
        dy,
        color: colors[idx % colors.length],
        size: 3 + Math.random() * 5,
        delay: Math.random() * 0.05
      };
    });
    setParticles((prev) => [...prev, ...newParticles]);
  };

  const words = a.split(" ");

  return (
    <motion.div
      initial={{ opacity: 0, y: 12 }}
      whileInView={{ opacity: 1, y: 0 }}
      viewport={{ once: false }}
      transition={{ delay: i * 0.05 }}
      animate={{
        y: open ? -4 : 0,
        boxShadow: open
          ? '0 16px 24px -8px rgba(99, 102, 241, 0.18), 0 8px 16px -10px rgba(99, 102, 241, 0.18)'
          : '0 1px 2px 0 rgba(0, 0, 0, 0.05)',
      }}
      whileHover={{
        y: open ? -4 : -2,
      }}
      whileTap={{ scale: 0.992 }}
      onMouseMove={handleMouseMove}
      onMouseEnter={() => setIsHovered(true)}
      onMouseLeave={() => setIsHovered(false)}
      className={`relative bg-bg-2 border rounded-2xl overflow-hidden cursor-pointer select-none transition-all duration-300 ${
        open 
          ? 'border-indigo-500/40 hover:border-indigo-500/50' 
          : 'border-border hover:border-indigo-500/25'
      }`}
    >
      {/* Dynamic Cursor Spotlight Glow */}
      {isHovered && (
        <motion.div
          className="pointer-events-none absolute -inset-px rounded-2xl opacity-100 transition-opacity duration-300 z-0"
          style={{
            background: `radial-gradient(180px circle at ${mousePos.x}px ${mousePos.y}px, rgba(99, 102, 241, 0.06), transparent 80%)`,
          }}
        />
      )}

      {/* Sliding accent border on the left */}
      <motion.div
        className="absolute left-0 top-0 bottom-0 w-[4px] bg-gradient-to-b from-indigo-500 to-cyan-500 z-10"
        initial={{ scaleY: 0 }}
        animate={{ scaleY: open ? 1 : 0 }}
        transition={{ type: "spring", stiffness: 300, damping: 25 }}
        style={{ originY: 0 }}
      />

      {/* Particles overlay container */}
      <div className="absolute inset-0 pointer-events-none overflow-hidden z-20">
        {particles.map(p => (
          <motion.span
            key={p.id}
            initial={{ x: p.x, y: p.y, scale: 0, opacity: 1 }}
            animate={{ 
              x: p.x + p.dx, 
              y: p.y + p.dy, 
              scale: [1, 1.4, 0], 
              opacity: [1, 1, 0] 
            }}
            transition={{ duration: 0.7, delay: p.delay, ease: "easeOut" }}
            onAnimationComplete={() => {
              setParticles(prev => prev.filter(item => item.id !== p.id));
            }}
            className="absolute rounded-full"
            style={{
              width: p.size,
              height: p.size,
              backgroundColor: p.color,
              boxShadow: `0 0 10px ${p.color}`,
              left: 0,
              top: 0
            }}
          />
        ))}
      </div>

      <button
        onClick={handleToggle}
        className="relative w-full flex items-center justify-between px-6 py-5 text-left gap-4 group focus:outline-none z-10"
      >
        <span className="font-heading font-bold text-[16px] text-slate-800 group-hover:text-indigo-650 transition-colors">
          {q}
        </span>
        <motion.div
          animate={{
            rotate: open ? 180 : 0,
            scale: open ? 1.15 : 1,
            color: open ? "rgb(99, 102, 241)" : "rgb(148, 163, 184)"
          }}
          transition={{ type: "spring", stiffness: 260, damping: 18 }}
        >
          <ChevronDown size={18} className="flex-shrink-0" />
        </motion.div>
      </button>

      <AnimatePresence initial={false}>
        {open && (
          <motion.div
            initial={{ height: 0, opacity: 0 }}
            animate={{ height: 'auto', opacity: 1 }}
            exit={{ height: 0, opacity: 0 }}
            transition={{ type: "spring", stiffness: 220, damping: 24 }}
            className="overflow-hidden relative z-10"
          >
            <motion.div
              variants={{
                hidden: {},
                show: {
                  transition: {
                    staggerChildren: 0.012,
                  }
                }
              }}
              initial="hidden"
              animate="show"
              exit="hidden"
              className="px-6 pb-5 text-[14px] text-slate-500 leading-relaxed border-t border-border pt-4 bg-bg-3/50 flex flex-wrap gap-x-1.5 gap-y-0.5"
            >
              {words.map((word, idx) => (
                <motion.span
                  key={idx}
                  variants={{
                    hidden: { opacity: 0, y: 8, filter: 'blur(3px)' },
                    show: { opacity: 1, y: 0, filter: 'blur(0px)' }
                  }}
                  transition={{ duration: 0.2, ease: "easeOut" }}
                  className="inline-block"
                >
                  {word}
                </motion.span>
              ))}
            </motion.div>
          </motion.div>
        )}
      </AnimatePresence>
    </motion.div>
  );
};

